package Model.Roles;

/**
 * Enum representing the different user types in the hospital system.
 */
public enum UserType {
    
    /**
     * Represents an Administrator user type.
     */
    ADMINISTRATOR,

    /**
     * Represents a Doctor user type.
     */
    DOCTOR,

    /**
     * Represents a Patient user type.
     */
    PATIENT,

    /**
     * Represents a Pharmacist user type.
     */
    PHARMACIST
}
